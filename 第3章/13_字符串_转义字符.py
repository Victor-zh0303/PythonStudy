print('在Python中，可以使用单引号包裹一个字符串')
# 将单引号改成'
print("在Python中，可以使用'包裹一个字符串")
# 使用\'输出'
print('在Python中，可以使用\'包裹一个字符串')
# 使用\"输出"
print("在Python中，可以使用\"包裹一个字符串")

# 使用\n进行换行
print('注册会员需要一下信息：\n姓名\n年龄\n手机号')

# 使用\\输出\
print('C:\\nice')

# 使用\b删除前一个字符，打字机效果
print('Helloo\b')

# 使用\r使光标回到本行开头，覆盖输出(进度加载效果）
print('67%\r68%')

# 使用\t表示水平制表符(让光标跳转到下一个制表位)
print('1234123412341234')
print('ab\tcd'.expandtabs(4))
print('abc\td'.expandtabs(4))
print('abcd\ta'.expandtabs(4))
print('我是\t中文')

print('1234123412341234')
print('姓名\t性别\t年龄')
print('张三\t男\t18')
print('李四\t女\t26')
